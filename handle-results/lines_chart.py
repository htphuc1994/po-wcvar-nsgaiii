def plot_multiple_lines(x, y_arrays):
    plt.figure(figsize=(20, 8))

    # Updated line styles list to include 6 styles
    # line_styles = ['-', '--', '-.', ':', 'dashed', 'dashed']
    # num_styles = len(line_styles)

